<?php
    //SUMAR
    function add($a, $b){
        echo $a.' + '.$b.' = '.$a+$b.'<br>';
    }

    add(4,6);

    //RESTAR
    function subtract($a, $b){
        echo $a.' - '.$b.' = '.$a-$b.'<br>';
    }

    subtract(14,6);


    //MULTIPLICAR
    function multiply ($a, $b){
        echo $a.' x '.$b.' = '.$a*$b.'<br>';
    }

    multiply (4,6);


    //DIVIDE
    function divide ($a, $b){
        echo $a.' / '.$b.' = '.$a/$b.'<br>';
    }

    divide (4,6);